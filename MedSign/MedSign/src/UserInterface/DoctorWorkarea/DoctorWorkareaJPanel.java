    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tbl_Appointment.getModel();
        dtm.setRowCount(0);
        for (Appointment a : appointmentControl.getAppointmentHistory().getDoctorAppointmentList((Doctor) userAccount.getPerson())) {
            Object[] row = new Object[3];
            row[0] = a;
            row[1] = a.getDateTime();
            row[2] = a.getStatus();
            dtm.addRow(row);
        }
    }

