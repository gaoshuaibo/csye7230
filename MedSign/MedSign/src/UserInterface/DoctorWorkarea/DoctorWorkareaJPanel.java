    private void btViewAppointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btViewAppointmentActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_Appointment.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
        Appointment appointment = (Appointment) tbl_Appointment.getValueAt(selectedRow, 0);
        ViewDoctorAppointmentJDialog jdialog = new ViewDoctorAppointmentJDialog(parent, true, appointment, appointmentControl.getUserAccountDirectory().getDoctorAccount());
        jdialog.setVisible(true);

    }//GEN-LAST:event_btViewAppointmentActionPerformed

    private void btActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btActionActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_Appointment.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
        Appointment appointment = (Appointment) tbl_Appointment.getValueAt(selectedRow, 0);
        if (appointment.getStatus().equals(Appointment.StatusType.Cancelled)) {
            JOptionPane.showMessageDialog(null, "This appointment is already cancelled by patient.");
            return;
        } else if (appointment.getStatus().equals(Appointment.StatusType.Closed)) {
            JOptionPane.showMessageDialog(null, "This appointment is already closed status.");
            return;
        }
        appointment.action();
        DatabaseFactory.getInstance().storeSystem();
        populateTable();
    }//GEN-LAST:event_btActionActionPerformed

